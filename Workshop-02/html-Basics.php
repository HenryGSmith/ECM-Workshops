    <h2 id="ecm1410">ECM 1410: Object-Oriented Programming</h2>
    <p>
      This module will introduce you to object-oriented problem-solving methods and provide you with object-oriented
      (OO) techniques for the analysis, design and implementation of solutions. We will introduce you to these concepts,
      and you will develop skills with a new programming language. By the end of this module, you will be able to apply
      these skills to design and implement small applications.
    </p>
    <hr>

    <h2 id="ecm1413">ECM 1413: Computers and the Internet</h2>
    <p>
      This module is designed to equip you with the foundational information you need to understand and work in business
