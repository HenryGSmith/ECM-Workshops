    <h2 id="ecm1407">ECM 1407: Social and Professional Issues of the Information Age</h2>
    <p>
      The module aims to provide you with the tools to reflect upon your role in the interface between digital
      technologies and society and on the moral and ethical use of information and information systems. By taking this
      module, you will become aware of your legal responsibilities and rights as an IT professional and as a user of
      digital technologies. The module will cover ethical theories, computer law and professional codes of conduct, and
      will address the ways in which broader areas of law (e.g. defamation, contracts, privacy and freedom of
      information legislation) impact upon technology users and IT professionals.
    </p>
